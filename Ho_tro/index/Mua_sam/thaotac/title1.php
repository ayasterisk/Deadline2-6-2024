<h2>Hướng dẫn quản lý địa chỉ trên FFShop</h2>

<h3>Trên Ứng dụng FFShop:</h3>

<p>Đăng nhập vào tài khoản FFShop.</p>

<p>Để thêm địa chỉ nhận hàng mới:</p>
<ul>
    <li>Chọn <em>Thêm địa chỉ mới</em>.</li>
    <li>Điền đầy đủ các thông tin cần thiết.</li>
    <li>Nhấn <em>Hoàn thành</em>.</li>
</ul>

<p>Để chỉnh sửa địa chỉ đã tồn tại:</p>
<ul>
    <li>Chọn địa chỉ cần sửa.</li>
    <li>Điền lại thông tin cần sửa đổi.</li>
    <li>Nhấn <em>Hoàn thành</em>.</li>
</ul>

<p>Để xóa địa chỉ đã tồn tại:</p>
<ul>
    <li>Chọn địa chỉ cần xóa.</li>
    <li>Chọn <em>Xóa địa chỉ</em>.</li>
    <li>Xác nhận xóa.</li>
</ul>

<h3>Trên Trang chủ FFShop:</h3>

<p>Vào Tài khoản của tôi (trong danh sách thả xuống khi chuyển con trỏ lên vị trí tên tài khoản).</p>

<p>Để thêm địa chỉ nhận hàng mới:</p>
<ul>
    <li>Chọn <em>Thêm địa chỉ mới</em>.</li>
    <li>Điền đầy đủ các thông tin cần thiết.</li>
    <li>Nhấn <em>Hoàn thành</em>.</li>
</ul>

<p>Để chỉnh sửa chi tiết của một địa chỉ nhận hàng đã tồn tại:</p>
<ul>
    <li>Chọn tính năng <em>Sửa</em> bên cạnh địa chỉ cần sửa.</li>
    <li>Điền lại thông tin cần sửa đổi.</li>
    <li>Nhấn <em>Hoàn thành</em>.</li>
</ul>

<p>Để xóa một địa chỉ nhận hàng đã tồn tại:</p>
<ul>
    <li>Chọn tính năng <em>Xóa</em> bên cạnh địa chỉ cần xóa.</li>
    <li>Xác nhận xóa.</li>
</ul>