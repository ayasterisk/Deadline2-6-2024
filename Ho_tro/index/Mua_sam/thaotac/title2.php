<h2>Hướng dẫn thêm Tài khoản/Thẻ ngân hàng trên FFShop</h2>

<h3>Trên ứng dụng FFShop:</h3>

<p>Đăng nhập vào tài khoản FFShop.</p>

<p>Để thêm Tài khoản/Thẻ ngân hàng:</p>
<ul>
    <li>Chọn <em>Tôi</em> > <em>Thiết lập tài khoản</em> > <em>Tài khoản/Thẻ ngân hàng</em>.</li>
    <li>Chọn <strong>Thêm Thẻ Tín dụng/Ghi nợ</strong> hoặc <strong>Thêm Tài khoản ngân hàng</strong> tùy vào lựa chọn của bạn.</li>
</ul>

<h4>A. Thêm Thẻ Tín dụng/Ghi nợ</h4>

<ul>
    <li>Sau khi gửi thông tin, bạn sẽ được chuyển đến trang web của Ngân hàng/Đơn vị cung cấp dịch vụ thẻ để xác minh thông tin.</li>
    <li>Nhập mã OTP được gửi đến điện thoại của bạn vào mục được yêu cầu để hoàn tất việc xác minh.</li>
</ul>

<h4>B. Thêm Tài khoản ngân hàng</h4>

<ul>
    <li>Sau khi thực hiện các bước trên, bạn sẽ được yêu cầu xác minh tài khoản bằng liên kết xác thực/mã OTP gửi qua tin nhắn tại ứng dụng Zalo hoặc điện thoại của bạn.</li>
    <li>Kiểm tra tin nhắn và nhập mã OTP được gửi để hoàn tất việc xác minh tài khoản.</li>
</ul>
