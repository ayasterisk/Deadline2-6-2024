<p>Câu Hỏi Thường Gặp Khi Trả Hàng Đổi Ý ?</p>


<a>1. Sản phẩm nào sẽ được trả hàng đổi ý (Hàng nguyên vẹn nhưng không còn nhu cầu)?<a>

Sản phẩm cần thỏa đồng thời các điều kiện sau để được trả hàng đổi ý:</a><br>

Có nhãn “Đổi ý miễn phí 15 ngày” tại trang thông tin sản phẩm<br>

Trong thời hạn trả hàng đổi ý: 15 ngày kể từ ngày nhận hàng<br>

Còn nguyên seal, tem, hộp sản phẩm<br>

Chưa qua sử dụng/lắp đặt<br>

Không bị làm rớt, bể vỡ, hư hỏng, trầy xước do lỗi của người mua<br>

<a>2. Tôi có được trả hàng đổi ý một/vài sản phẩm trong đơn hàng không?</a>

Trong trường hợp yêu cầu trả hàng đổi ý, bạn cần đảm bảo TẤT CẢ sản phẩm trong yêu cầu đều thỏa điều kiện trả hàng đổi ý

Nếu đơn hàng của bạn bao gồm cả sản phẩm thỏa và không thỏa điều kiện để trả hàng đổi ý, bạn vui lòng tạo yêu cầu trả hàng đổi ý riêng cho các sản phẩm thỏa điều kiện. Các sản phẩm không thỏa điều kiện cần được trả hàng bằng lý do khác theo đúng quy định của Shopee

<a>3. Mất bao lâu để tôi nhận lại tiền sau khi gửi trả hàng thành công? Vui lòng xem hướng dẫn chi tiết tại bài viết [Hoàn tiền] Mất bao lâu để tôi nhận lại tiền sau khi gửi trả hàng thành công?</a>

 

<a>4. Tại sao tôi không nhìn thấy nhãn "Đổi ý miễn phí 15 ngày"?</a>

Shopee đã cập nhật nhãn "Đổi ý miễn phí 15 ngày" trên phiên bản ứng dụng mới nhất. Bạn hãy cập nhật ứng dụng Shopee để nhãn "Đổi ý miễn phí 15 ngày" được hiển thị đầy đủ cho các sản phẩm thỏa điều kiện.


