<h2>Hướng dẫn đánh giá sản phẩm trên FFShop</h2>

<p>
    Bạn có thể đánh giá mỗi sản phẩm trong 1 đơn hàng 1 lần duy nhất ngay sau khi đơn hàng đã chuyển sang mục ‘Đã giao’ hoặc ngay sau khi bạn bấm ‘Đã nhận được hàng’.
</p>

<p>
    Để đánh giá một sản phẩm, vui lòng đăng nhập vào website FFShop và làm theo những bước sau: Truy cập Tôi &gt; Đánh giá (thuộc mục Đơn mua) &gt; chọn Đánh giá trong thẻ của sản phẩm mà bạn muốn đánh giá.
</p>
