<h2>Hướng dẫn sử dụng và bảo quản sản phẩm</h2>

<h3>Đối với sofa da thật:</h3>
<ul>
    <li>Không để bề mặt da tiếp xúc với nước.</li>
    <li>Không để trẻ em chạy, nhảy trên ghế sofa.</li>
    <li>Tránh để vật có cạnh sắt nhọn lên sản phẩm để tránh trầy xước.</li>
    <li>Không đặt sofa ở những nơi có nhiệt độ quá nóng, hoặc nơi ẩm ướt.</li>
    <li>Không sử dụng các chất vệ sinh, tẩy rửa thông thường để làm sạch sofa.</li>
</ul>

<h3>Đối với nội thất gỗ:</h3>
<ul>
    <li>Không để bề mặt gỗ tiếp xúc với nước quá lâu, lau khô khi nước bị đổ lên bề mặt gỗ.</li>
    <li>Không để khăn ướt/khăn ẩm lên bề mặt gỗ trong thời gian dài.</li>
    <li>Tránh đặt sản phẩm ở những nơi có ánh nắng trực tiếp chích vào, hoặc nơi có độ ẩm cao.</li>
    <li>Không nên dùng hoá chất tẩy rửa để lau chùi sản phẩm vì hoá chất sẽ làm hư hại bề mặt sản phẩm.</li>
</ul>

